my ($doit,$i,$j,$name) = (0,0,0,'');

while(<>) {
    if (/^Lookup:.*Kerning in Latin/) {
	s/\)/'hang' <'KOR ' 'dflt' > \)/;
    }
    # first glyph of the old hangul variant block in v2.120
    if (/StartChar\:\s+Oldhangul_ChoFiller\s*$/) {
	$doit = 1;
	$i = 0;
	$j = 0;
    }
    if ($doit and $OLDJAMO[$i] and /StartChar\:/) {
	$name = sprintf ("uni%04X.y%d",$OLDJAMO[$i],$j);
	$_ = "StartChar: $name\n";
	$j++;
	if($j >= $OLDSET[$i]) {
	    $i++;
	    $j = 0;
	}
    }
    print;
}
